class Atom:
    def __init__(self, num, name, res, id, resNum, x, y, z, charge, radius):
        self.Number        = num
        self.Name          = name
        self.Residue       = res
        self.ChainID       = id
        self.ResidueNumber = resNum
        self.X             = x
        self.Y             = y
        self.Z             = z
        self.Charge        = charge
        self.Radius        = radius
        
